package practica_presencial;

public class arreglo_datos {

    public String numero = new String();
    public String nombre = new String();
    public String direccion = new String();
    public String correo = new String();

    public arreglo_datos(String numero, String nombre, String direccion, String correo) {
        this.numero = numero;
        this.nombre = nombre;
        this.direccion = direccion;
        this.correo = correo;
    }

}
